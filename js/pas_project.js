const pas_url_1300_700 = "https://finds.org.uk/database/search/results/fromdate/-1300/todate/-700/format/json";

async function getHorte_SBZ_FEZ() {
    const response = await fetch(pas_url_1300_700);
    const data = await response.json();
    const ba_nr = data.facets.broadperiod["BRONZE AGE"]         //
    const ea_nr = data.facets.broadperiod["IRON AGE"]           //
    const total_ba_ea = data.meta["totalResults"]               //
    const published_ba_ia = data.facets.workflow["3"];
    const validation_ba_ia = data.facets.workflow["4"];
    const objecttype_ba_ia = data.facets.objectType;
    document.getElementById("ba_nr").textContent = ba_nr;
    document.getElementById("ea_nr").textContent = ea_nr;
    document.getElementById("total_ba_ea").textContent = total_ba_ea;
    document.getElementById("published_ba_ia").textContent = published_ba_ia;
    document.getElementById("validation_ba_ia").textContent = validation_ba_ia;
    const byCounty = data.facets.county;
    counties_data = data.facets.county;
    const objectType = data.facets.objectType;
    for (var x in byCounty) {
        ByCounty_labels.push(x);
    }
    for (var x in byCounty) {
        ByCounty_numbers.push(byCounty[x]);
    }

    for (var y in objectType) {
        Objecttype_labels.push(y);
    }
    for (var y in objectType) {
        Objecttype_numbers.push(objectType[y]);
    }
}

